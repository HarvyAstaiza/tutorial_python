# Crear una lista de carros

carros = ["Toyota Corolla", "Honda Civic", "Ford Mustang", "Chevrolet Camaro"]

def listarCarros(cars):
    for carro in cars:
        print(carro)

#ejemplo uso
listarCarros(carros)


